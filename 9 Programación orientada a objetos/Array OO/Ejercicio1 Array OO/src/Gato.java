public class Gato {
    
    private String nombre;
    private String raza;
    private String sexo;
    
    public String getNombre() {
        return nombre;
    }
    
    public void setNombre(String n) {
        this.nombre = n;
    }
    
    public String getRaza() {
        return raza;
    }
    
    public void setRaza(String r) {
        this.raza = r;
    }
    
    public String getSexo(){
        return sexo;
    }
    
    public void setSexo(String s){
        this.sexo = s;
    }
}
